package modelo;
public class Animales 
{
    private String tigre;
    private String elefante;
    private String aguila;
    private String loro;
    private String Koala;


    public String getAguila() 
    {
        return aguila;
    }

    public String getElefante()
    {
        return elefante;
    }

    public String getKoala() 
    {
        return Koala;
    }

    public String getLoro() 
    {
        return loro;
    }

    public String getTigre() 
    {
        return tigre;
    }

    public void setAguila(String aguila) 
    {
        this.aguila = aguila;
    }

    public void setElefante(String elefante) 
    {
        this.elefante = elefante;
    }

    public void setKoala(String koala) 
    {
        Koala = koala;
    }

    public void setLoro(String loro) 
    {
        this.loro = loro;
    }

    public void setTigre(String tigre) 
    {
        this.tigre = tigre;
    }
}
