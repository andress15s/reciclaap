package modelo;

public class Frutas 
{
    private String banano;
    private String manzana;
    private String fresa;
    private String naranja;
    private String patilla;

    public String getBanano() 
    {
        return banano;
    }

    public String getFresa() 
    {
        return fresa;
    }

    public String getManzana() 
    {
        return manzana;
    }

    public String getNaranja() 
    {
        return naranja;
    }

    public String getPatilla() 
    {
        return patilla;
    }

    public void setBanano(String banano) 
    {
        this.banano = banano;
    }

    public void setFresa(String fresa) 
    {
        this.fresa = fresa;
    }

    public void setManzana(String manzana) 
    {
        this.manzana = manzana;
    }

    public void setNaranja(String naranja) 
    {
        this.naranja = naranja;
    }

    public void setPatilla(String patilla) 
    {
        this.patilla = patilla;
    }
}
